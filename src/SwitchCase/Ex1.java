package SwitchCase;

public class Ex1 {
public static void main(String[] args) {
	
	String Day="Monday";
	switch (Day) {
	case "Monday":System.out.println("First Day Of Week");break;
	case "Tue":System.out.println("Second Day Of Week");break;
	case "Wed":System.out.println("Third Day Of Week");break;
	case "Thrs":System.out.println("Fourth Day Of Week");break;
	case "Fri":System.out.println("Fifth Day Of Week");break;
	case "Sat":System.out.println("Sixth Day Of week");break;
	case "Sun":System.out.println("Last Day Of Week");break;
	default :System.out.println("No Day");
	
	}
}
}
